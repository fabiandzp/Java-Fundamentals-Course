package d_sentencias_de_control;

public class IfElse {
    public static void main(String[] args){
        int numero = 2;

        if (numero == 1) {
            System.out.println ("numero 1");
        }
        else if (numero == 2) {
            System.out.println ("numero 2");
        }
        else if (numero == 3) {
            System.out.println ("numero 3");
        }
        else {
            System.out.println ("numero desconocido");
        }
    }
}
